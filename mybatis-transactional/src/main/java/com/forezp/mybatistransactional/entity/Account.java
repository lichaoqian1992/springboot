package com.forezp.mybatistransactional.entity;

import java.io.Serializable;

public class Account implements Serializable {

    private static final long serialVersionUID = -310429821558658388L;

    private int id ;
    private String name ;
    private double money;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getMoney() {
        return money;
    }

    public void setMoney(double money) {
        this.money = money;
    }
}
